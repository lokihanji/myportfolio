// New Searchable Select Components
function SelectSearchInput({
  placeholder = "Search...",
  className,
  ...props
}: React.ComponentProps<"input">) {
  return (
    <div className="relative p-2">
      <Search className="absolute left-4 top-1/2 h-4 w-4 -translate-y-1/2 text-muted-foreground" />
      <input
        className={cn(
          "flex h-9 w-full rounded-md border border-input bg-transparent px-3 py-1 pl-9 text-sm shadow-sm transition-colors placeholder:text-muted-foreground focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-ring disabled:cursor-not-allowed disabled:opacity-50",
          className
        )}
        placeholder={placeholder}
        {...props}
      />
    </div>
  )
}

function SelectSearchableContent({
  children,
  searchValue,
  onSearchChange,
  searchPlaceholder = "Search...",
  className,
  ...props
}: React.ComponentProps<typeof SelectContent> & {
  searchValue: string
  onSearchChange: (value: string) => void
  searchPlaceholder?: string
}) {
  return (
    <SelectContent className={cn("p-0", className)} {...props}>
      <SelectSearchInput
        value={searchValue}
        onChange={(e) => onSearchChange(e.target.value)}
        placeholder={searchPlaceholder}
        className="border-0 focus-visible:ring-0"
      />
      <div className="max-h-60 overflow-auto">
        {children}
      </div>
    </SelectContent>
  )
}

